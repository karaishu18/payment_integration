        <h2>About Us</h2>
        <p>“Service of oppressed humanity is service of the almighty”

         Established in 1985, Narayan Seva Sansthan is the Top Nonprofit Trust headquartered in Udaipur, Rajasthan India. The organization started
          3 decades ago with a vision to serve the needy and cure the helpless patients through free corrective surgeries with a will to fight a 
          battle with Polio and other related birth disabilities. An internationally renowned center with more than 12 specialized hospitals, 
          1100+ beds, food for 4500+ people every day, and more than 424050 free of cost corrective surgeries; the Sansthan has become more like a 
          family and home to all the patients and their visitors.
         The free of cost Disability Treatment at Narayan Seva Sansthan is inclusive of various offerings listed below:--<br>
         1. Treating crippling polio patients through 424050 and counting corrective surgeries, an attempt to make a crawling individual walk without 
         any crutch or support.<br>
         2. Not only providing them a physical aid but ensuring their economical aid through the teaching of various skills and vocational learning 
         which in fact transforms them to be more confident and evolved.<br>
         3. Regular conduction of 'mass marriage ceremonies twice a year for young marriageable 'differently able' and poor boys & girls. To date, a 
         total of 2109 such pairs have been tied in the 'lifelong' knot of marriage with the efforts of the organization. All these couples are leading 
         a happy married life.<br>
         4. Polio diagnosis and surgery service camps held at different places organized by Narayan Seva Sansthan have crossed numbers to a great extent. 
         More than 3547 diagnostic & 522 corrective surgery camps have been organized for the selection & treatment of polio-affected people.<br>
         5. 300-400 patients checked and diagnosed every day, 80-90 corrective surgeries are successfully done and also provided with helping aids viz. 
         artificial limbs, crutches, calipers, tricycles, wheelchairs, hearing aids, blind sticks, etc. among poor and the needy ones. More than 
         1260627 people have been benefitted from the same to date.<br>
         6. For a speedy recovery, physiotherapy, and other post-operation procedures and workshops are available for the patients and the ashrams 
         for the same have crossed the count of 30 cities.<br>
         7. Narayan Children Academy established to provide education to financially weak students. Also, a separate school for specially-abled 
         children with special software like JAWS and other customized amenities. Accommodation, food, and clothing are free of cost provided the 
         latter kind of children.<br>
         8. Narayan Seva Sansthan works for ‘the change’ in the society we desire for.</p>
